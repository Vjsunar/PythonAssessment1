#Operations on strings - count, cases, start, end

# count  // The count() method returns the number of times a specified value appears in a string or list

s1= " my name is vijay"
s2="a"
print(s1.count(s2))
print(f"{s2} is present {s1.count(s2)} times ")

# case  // upper , lower ,

text ="lets learn Python "
print(text.upper())
print(text.lower())
print(text.title())
print(text.capitalize())
print(text.casefold())
print(text.swapcase())

